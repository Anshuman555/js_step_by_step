// Primitive Dta types => NN SS BB U
// =========

// Number
let a = 1;
console.log(typeof (a));

// Null
let b = null;
console.log(typeof (b));

// Bollean
let c = true;
console.log(typeof (c));

// BigInt
let d = BigInt("123");
console.log(typeof (d));

// String
let e = "Alex";
console.log(typeof (e));

// Symbol
let f = Symbol("I am Symbol");
console.log(typeof (f));

// Undefined
let g = undefined;
console.log(typeof (g));

// Objects (Non Primitive Data Type) 
// ==========

const studendDetails = {
    "Alex": true,
    "Nathan": false,
    "Parker": false
};

console.log(studendDetails.Alex);
console.log(studendDetails.test);

